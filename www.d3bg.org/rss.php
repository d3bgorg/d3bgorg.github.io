Първото продължение на Diablo IV ще пристигне на 8 октомври и ще добави нова зона, нов клас, нови подземия в стил raid-ове, продължение на историята и още. Много от новите неща ще бъдат директно взети от Диабло 2 и Диабло 3, но леко изменени. Като например връщането Руническите думи, докато Glyph системата ще бъде изменена и ще заприлича на системата с легендарните камъни от Диабло 3. Ще бъдат добавени нови трудности и ще видим промени по Парагон системата. 

[color=orange][font=OldFenris-Regular][size=5]Нов максимален левел 60[/size][/font][/color]

Новият максимален левел на героите ще бъде променен от 100 на 60, а парагон нивата ще започнат да се трупат чак след 60-то ниво, а не както сега - след 50. Парагон нивата ще бъдат споделени с целия акаунт, но новите герои ще отключват бонусите от парагон нивата едва когато достигнат 60-то ниво. 

[color=orange][font=OldFenris-Regular][size=5]Нови трудности [/size][/font][/color]
[img]https://www.d3bg.org/img/upload/d4patch2.0/VKF78AAO3QPD1724871462993.jpg[/img]

Трудността е изцяло преработена. Промяната на трудноста ще добавя дебъф към резистите и армора на героите, докато в същото време ще увеличава демиджа и кръвта на противниците. От играчите ще зависи дали ще искат да играят по-лежерно или да им бъде трудно, но за сметка на това да получават повече награди. Докато левелвате, ще можете да избирате между Normal, Hard, Expert и Penitent. След левел 60, ще се отключват Torment 1, Torment II, Torment III и Torment IV. 

[color=orange][font=OldFenris-Regular][size=5]Преработка на подземията[/size][/font][/color]

The Pit ще заприлича още повече на Greater Rift-овете от Диабло 3. С него ще бъдат ъпгрейдвани Глифовете, а те от своя страна ще получат промени - максималното им ниво ще бъде увеличено от 25 на 100. В същото време Nightmare Dungeon-ите пък ще бъдат мястото, където ще се снабдяваме със съставките необходими за Masterwork на предметите. 

[color=orange][font=OldFenris-Regular][size=5]Заръщане на Руническите думи[/size][/font][/color]
[img]https://www.d3bg.org/img/upload/d4patch2.0/63DUZR7PCDHK1724871462642.jpg[/img]

